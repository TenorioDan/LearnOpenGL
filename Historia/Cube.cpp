void Cube::translate(GLfloat x, GLfloat y, GLfloat z)
{
	_model = glm::translate(_model, glm::vec3(x, y, z));
}

void Cube::update(float currentTime)
{
	GameObject::update(currentTime);
}

void Cube::render(glm::mat4 VPMatrix, glm::vec3 viewPos, glm::vec3 lightPos, glm::vec3 lightColor)
{
	glUseProgram(_lightShader->Program());
	_lightShader->setMat4("MVP", VPMatrix * _model);
	_lightShader->setMat4("model", _model);
	_lightShader->setVec3("objectColor", _color);
	_lightShader->setVec3("lightColor", lightColor);
	_lightShader->setVec3("lightPos", lightPos);
	_lightShader->setVec3("viewPos", viewPos);

	glBindVertexArray(_vao);
	glDrawArrays(GL_TRIANGLES, 0, 36);
}