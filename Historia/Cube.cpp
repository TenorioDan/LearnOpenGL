void Cube::translate(GLfloat x, GLfloat y, GLfloat z)
{
	_model = glm::translate(_model, glm::vec3(x, y, z));
}

void Cube::update(float currentTime)
{
	GameObject::update(currentTime);
}

void Cube::render(glm::mat4 VPMatrix, Shader& lightShader)
{
	// TODO these values should only be set once for this object not every time render is called
	lightShader.setInt("material.diffuse", 0);
	lightShader.setInt("material.specular", 1);
	lightShader.setInt("material.emission", 2);
	lightShader.setFloat("material.shininess", 32.0f);

	lightShader.setMat4("MVP", VPMatrix * _model);
	lightShader.setMat4("model", _model);

	lightShader.setFloat("angleSin", sin((_currentTime * 5 * _deltaTime) / 2));
	lightShader.setFloat("angleCos", cos((_currentTime * 5 * _deltaTime) / 2));

	glActiveTexture(GL_TEXTURE0);
	glBindTexture(GL_TEXTURE_2D, _diffuseMap);
	glActiveTexture(GL_TEXTURE1);
	glBindTexture(GL_TEXTURE_2D, _specularMap);

	if (_emissionMap > 0)
	{
		glActiveTexture(GL_TEXTURE2);
		glBindTexture(GL_TEXTURE_2D, _emissionMap);
	}
	
	glBindVertexArray(_vao);
	glDrawArrays(GL_TRIANGLES, 0, 36);
}

void Cube::addTexture(const char* diffusePath, const char* specularPath, const char* emissionPath)
{
	 // Diffuse map and specular map are require for textures
	_diffuseMap = loadTexture(diffusePath);
	_specularMap = loadTexture(specularPath);

	if (emissionPath != NULL)
	{
		_emissionMap = loadTexture(emissionPath);
	}
}